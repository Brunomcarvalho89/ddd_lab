package com.resow.authenticationidentity.application.command;

import com.resow.common.application.command.CommandBase;

/**
 *
 * @author Bruno Carvalho - brunomcarvalho89@gmail.com
 */
public class ChangeAddressDescriptionCommand implements CommandBase {

    private String nickname;
    private String addressDescription;

    public ChangeAddressDescriptionCommand(String nickname, String addressDescription) {
        this.nickname = nickname;
        this.addressDescription = addressDescription;
    }

    public String getNickname() {
        return nickname;
    }

    public void setNickname(String nickname) {
        this.nickname = nickname;
    }

    public String getAddressDescription() {
        return addressDescription;
    }

    public void setAddressDescription(String addressDescription) {
        this.addressDescription = addressDescription;
    }
}
